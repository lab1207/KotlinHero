fun main(){

    charSayHello()
    booleanSayHello()

}

fun charSayHello(){

    val charA : Char = 'A'
    val charB : Char = 'B'

    println("The CharA = $charA ")
    println("The CharB = $charB ")

}

fun booleanSayHello(){

    val booleanA : Boolean = true
    val booleanB : Boolean = false

    println("The booleanA = $booleanA")
    println("The booleanB = $booleanB")

}