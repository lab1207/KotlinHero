fun main(){

    floatDoubleCalculation()
    floatDoublePrintValue()

}

fun floatDoubleCalculation (){

    val a = 2.9F
    val b = 45.9

    println("a + b = ${a + b}")
    println("a - b = ${a - b}")
    println("a * b = ${a * b}")
    println("a / b = ${a / b}")
    println("a % b = ${a % b}")

}

fun floatDoublePrintValue(){

    val a: Float = 44F
    val b: Double = 34.9

    println("The Value Of Float a = $a")
    println("The Value Of Double b = $b")

}